let pokemon = ["Pikachu", "Charmander", "Bulbassaur"]

// o comando abaixo serve para excluir o último item da lista acima
pokemon.pop()

// o comando abaixo serve para excluir o primeiro item da lista acima
pokemon.shift()

// abaixo serve para aparecer toda a lista de Pokemon
console.log(pokemon)

// abaixo serve para aparecer apenas um Pokemon da lista
// a lista sempre começa do número zero (0)
console.log(pokemon[0])