// Estrutura de decisão
// switch

let fruta = "banana"

switch (fruta) {
    case "laranja":
       console.log("Suco de laranja")
       break
    
    case "banana":
       console.log("Vitamina de banana")
       break // o "break" é usado para evitar que o código continue executando os próximos cases.

    case "maçã":
        console.log("Suco de maçã")
        break
}


